
public class escapeCharacters {

	public static void main(String[] args) {
		
		// \n - newline escape character
		System.out.println("This is an example of using \na new line escape character.");
		
		// \t - tab escape character
		System.out.println("Example \t of \t tabs.");

		// \" and \' - quote escape characters
		System.out.println("\"The Lion King\" or \'Hello World\'");
		
		// \\ - Backslash escape character
		System.out.println("\\\\\\");
		
	}

}
